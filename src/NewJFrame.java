    private void btnentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnentActionPerformed
        // TODO add your handling code here:
        DecimalFormat df = new DecimalFormat("0.00");
        double total;
        double subtotal =0;
        final double TEXTRATE=.08;
        double tax;
        
                if (radiocoffee.isSelected()){
                    subtotal=subtotal+4.5;
                   
                }else if(radiocap.isSelected()){
                    subtotal=subtotal+3.5;
                }else if(radioiced.isSelected()){
                 subtotal=subtotal+9.8;   
                } else if (radiotea.isSelected()){
                    subtotal=subtotal+2;
                }
                if (checkcrepe.isSelected()){
                   subtotal=subtotal+10;
                }if(checkgazouz.isSelected()){
                   subtotal=subtotal+3.5;
                }if (checksal.isSelected()){
                   subtotal=subtotal+13;
                }
                if(checkpizza.isSelected()){
                   subtotal=subtotal+16;
                }
                subtext.setText(Double.toString(subtotal));
                subtotal =Double.parseDouble(subtext.getText());
                tax= subtotal+TEXTRATE;
                total = tax+subtotal;
                taxtext.setText(Double.toString(tax));
                totaltext.setText(Double.toString(total));
                taxtext.setText(df.format(tax));
                totaltext.setText(df.format(total));
               subtext.setText(df.format(subtotal));
                     
    }//GEN-LAST:event_btnentActionPerformed

